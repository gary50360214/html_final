            <div class="col-sm-12 col-md-6">
                <b>課程簡介</b>
                <p class = "info">社會企業之基本定義在於兼顧社會價值與獲利能力之組織實踐，因此本課程有兩個主要取向，一是「社會價值」，引導學生關心社會，對社會需求與社會議題有所體悟，進而形成實踐之價值主張與構想；另一方面則是「商業營運」，使學生理解如何將社會價值轉化為企業運作，建立商業模式並擴大影響力。本課程之教學理念在於由實務之真實案例引出理論觀念，由實務帶入社會與商業並重之新典範思維，重視社會價值結合企業模式操作之實務，培養跨領域整合能力，提升社會關懷的意識、能力與行動力。</p>
                <b>課程目標</b>
                <p class = "info">1.經由多個社會企業創業家親身說明其創業歷程與經營特色，使學生理解社會企業兼顧社會價值與獲利能力之基本意涵。
                    <br>2.本課程透過案例對照與觀念啟發，使學員獲得社會企業整合實務與理論之整體理解。</p>
                <b>適用對象</b>
                <p class = "info">對本課程有興趣的學習者</p>
                <b>課程進度</b>
                <p class = "info">第一週：讓夢想起飛的教育家 觀念講解 (社企發展、價值主張) 
                    <br>第二週：文化是社會創業的使命與沃土(台南土溝村) 文化是社會創業的使命與沃土(台南信義老街) 觀念講解 (定義、影響力) 
                    <br>第三週：土石沖不走的是希望 觀念講解(社企價值創造、成本收益估算) 
                    <br>第四週：社會企業促進農業型社區之轉型(印尼亞齊特區) 以原住民為主體的光原社會企業 觀念講解(社區與社會企業一社區培力) 
                    <br>第五週：後山在對你微笑(宜蘭討海文化) 後山在對你微笑(大王菜舖子) 觀念講解(社區與社會企業─社區資源條件與社企介入策略) 
                    <br>第六週：溫良的人是有福的(慢飛兒社會企業) 懷抱四方的四方報 觀念講解(B型企業) 
                    <br>第七週：社群力量大 (行動 x 公益 x 網路) 觀念講解(社會影響力債券、簡約創新) 
                    <br>第八週：在黑暗中學對話 從天空的院子到小鎮的文創 觀念講解(服務為核心理念) 
                    <br>第九週：社企構想加速器</p>
